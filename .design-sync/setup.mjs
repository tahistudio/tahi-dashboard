// Non-component exports to also surface on window.TahiDS (helpers used by
// previews / the design agent). Keyed by source file.
const EXTRA = {
  badge: ['statusTone', 'priorityTone'],
  toast: ['useToast'],
}

// Family files hold many small exports sharing one header doc — let their
// prompt synthesize from props + authored preview examples instead of pasting
// the shared header onto every sibling.
const FAMILY_FILES = new Set(['chart', 'tahi-glyphs', 'animated-icons'])

// ── Barrel entry ────────────────────────────────────────────────────────────
const byFile = new Map()
for (const [name, file] of M) {
  if (!byFile.has(file)) byFile.set(file, [])
  byFile.get(file).push(name)
}
let barrel =
  '// AUTO-GENERATED by .design-sync/setup.mjs — DS bundle entry (curated primitives).\n' +
  '// Re-exports only the design-system components from components/tahi/* so esbuild\n' +
  '// produces a clean, app-decoupled IIFE. Do not edit by hand; edit setup.mjs.\n\n'
for (const [file, names] of byFile) {
  const all = [...names, ...(EXTRA[file] ?? [])]
  barrel += `export { ${all.join(', ')} } from '@/components/tahi/${file}'\n`
}
writeFileSync(join(DS, 'ds-entry.tsx'), barrel)

// ── Next.js stubs (neutralize next/link, next/image, next/navigation) ───────
writeFileSync(join(DS, 'stubs', 'next-link.tsx'),
`import * as React from 'react'

type LinkProps = Omit<React.AnchorHTMLAttributes<HTMLAnchorElement>, 'href'> & {
  href?: string | { pathname?: string }
  prefetch?: boolean
  replace?: boolean
  scroll?: boolean
  shallow?: boolean
  passHref?: boolean
  locale?: string | false
  legacyBehavior?: boolean
}

const Link = React.forwardRef<HTMLAnchorElement, LinkProps>(function Link(
  { href, prefetch, replace, scroll, shallow, passHref, locale, legacyBehavior, children, ...rest },
  ref,
) {
  const h = typeof href === 'string' ? href : href?.pathname ?? '#'
  return <a ref={ref} href={h} {...rest}>{children}</a>
})

export default Link
`)

writeFileSync(join(DS, 'stubs', 'next-image.tsx'),
`import * as React from 'react'

type ImageProps = Omit<React.ImgHTMLAttributes<HTMLImageElement>, 'src'> & {
  src: string | { src: string }
  fill?: boolean
  priority?: boolean
  quality?: number
  placeholder?: string
  blurDataURL?: string
  loader?: unknown
  unoptimized?: boolean
}

const Image = React.forwardRef<HTMLImageElement, ImageProps>(function Image(
  { src, fill, priority, quality, placeholder, blurDataURL, loader, unoptimized, style, ...rest },
  ref,
) {
  const s = typeof src === 'string' ? src : src?.src
  const st: React.CSSProperties = fill
    ? { position: 'absolute', inset: 0, width: '100%', height: '100%', objectFit: 'cover', ...style }
    : (style ?? {})
  return <img ref={ref} src={s} style={st} {...rest} />
})

export default Image
`)

