// ── Per-component doc files (group frontmatter + extracted header JSDoc) ─────
function extractTopJsdoc(src) {
  const m = /\/\*\*([\s\S]*?)\*\//.exec(src)
  if (!m) return ''
  return m[1].split('\n').map((l) => l.replace(/^\s*\*\s?/, '')).join('\n').trim()
}
const componentSrcMap = {}
const docsMap = {}
const srcCache = new Map()
for (const [name, file, group] of M) {
  componentSrcMap[name] = `components/tahi/${file}.tsx`
  const rel = `.design-sync/docs/${name}.md`
  docsMap[name] = rel
  let body = ''
  if (!FAMILY_FILES.has(file)) {
    if (!srcCache.has(file)) srcCache.set(file, readFileSync(join(REPO, 'components', 'tahi', `${file}.tsx`), 'utf8'))
    body = extractTopJsdoc(srcCache.get(file))
  }
  const fm = `---\ncategory: ${group}\n---\n`
  writeFileSync(join(REPO, rel), body ? `${fm}\n${body}\n` : fm)
}

