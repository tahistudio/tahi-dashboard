// design-sync scaffolding generator for tahi-dashboard.
//
// tahi-dashboard is a Next.js app, not a published component library, so the
// converter runs in "package" shape with an explicit barrel --entry that
// re-exports ONLY the curated design-system primitives (the set showcased in
// app/(dashboard)/design-system/design-system-content.tsx). This keeps the
// esbuild IIFE bundle decoupled from the app's server/Next/Clerk code.
//
// Regenerate after editing the manifest below:  node .design-sync/setup.mjs
// (Also run  node .design-sync/compile-css.mjs  to refresh compiled.css.)

